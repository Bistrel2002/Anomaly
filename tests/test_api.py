def test_root():
    """Verify that the health-check endpoint returns 200 with the expected message."""
    with client:
        response = client.get("/")
    assert response.status_code == 200
    assert response.json() == {"message": "✅ Fraud Detection API is UP and Running!"}


def test_predict_normal_transaction():
    """Send 5 legitimate transactions and assert the model classifies them as normal.

    Each payload is a real record from ``creditcard.csv`` known to be
    non-fraudulent.  We verify:
        - HTTP 200 status.
        - ``is_fraud`` is ``False``.
        - ``fraud_probability`` is present.
    """
    for payload in NORMAL_TRANSACTIONS:
        with client:
            response = client.post("/predict", json=payload)

        assert response.status_code == 200
        data = response.json()

        assert "is_fraud" in data
        assert "fraud_probability" in data
        assert data["is_fraud"] is False


def test_predict_fraud_transaction():
    """Send 5 fraudulent transactions and assert the model detects them.

    Each payload is a real fraud record from ``creditcard.csv``.  We
    verify:
        - HTTP 200 status.
        - ``is_fraud`` is ``True``.
        - ``fraud_probability`` ≥ 0.50.
    """
    for payload in FRAUD_TRANSACTIONS:
        with client:
            response = client.post("/predict", json=payload)

        assert response.status_code == 200
        data = response.json()

        assert data["is_fraud"] is True
        assert data["fraud_probability"] >= 0.50


def test_predict_missing_feature():
    """Verify that a payload missing ``Amount`` is rejected with HTTP 422.

    Pydantic validation should block the request before it reaches the
    model, returning a structured error pinpointing the missing field.
    """
    invalid_payload = {
        "Time": 0.0,
        "V1": -1.3, "V2": -0.07, "V3": 2.5, "V4": 1.3, "V5": -0.3,
        "V6": 0.4, "V7": 0.2, "V8": 0.09, "V9": 0.3, "V10": 0.09,
        "V11": -0.5, "V12": -0.6, "V13": -0.9, "V14": -0.3, "V15": 1.4,
        "V16": -0.4, "V17": 0.2, "V18": 0.02, "V19": 0.4, "V20": 0.2,
        "V21": -0.01, "V22": 0.2, "V23": -0.1, "V24": 0.06, "V25": 0.1,
        "V26": -0.18, "V27": 0.13, "V28": -0.02,
        # Amount intentionally omitted.
    }

    with client:
        response = client.post("/predict", json=invalid_payload)

    assert response.status_code == 422

    data = response.json()
    assert "detail" in data
    assert data["detail"][0]["loc"] == ["body", "Amount"]
    assert data["detail"][0]["type"] == "missing"
